static const char* TAG = "display";

// ─── panel geometry & pins (T-Dongle-C5 defaults from LilyGo pin_config.h) ─────
static constexpr int W = 80;
static constexpr int H = 160;
#define PIN_MOSI  CONFIG_TESLA_DISPLAY_SPI_MOSI
#define PIN_SCK   CONFIG_TESLA_DISPLAY_SPI_SCK
#define PIN_CS    CONFIG_TESLA_DISPLAY_SPI_CS
#define PIN_DC    CONFIG_TESLA_DISPLAY_DC
#define PIN_RST   CONFIG_TESLA_DISPLAY_RST
#define PIN_BL    CONFIG_TESLA_DISPLAY_BL
// ST7735S 0.96" panels are offset within the controller's 132x162 RAM. These
// match the common LilyGo/Adafruit 0.96" module; tune on first flash if the image
// is shifted or the colours are inverted (see CONFIG_TESLA_DISPLAY_*_OFFSET).
#define X_OFF     CONFIG_TESLA_DISPLAY_X_OFFSET
#define Y_OFF     CONFIG_TESLA_DISPLAY_Y_OFFSET

// ─── colours (RGB565, natural order; byte-swapped at flush) ───────────────────
static inline uint16_t rgb565(uint8_t r, uint8_t g, uint8_t b) {
    return (uint16_t)(((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3));
}
static const uint16_t C_BG     = rgb565(244, 245, 247);
static const uint16_t C_CARD   = rgb565(255, 255, 255);
static const uint16_t C_INK    = rgb565(27, 27, 27);
static const uint16_t C_GREY   = rgb565(140, 146, 154);
static const uint16_t C_GREEN  = rgb565(47, 158, 68);
static const uint16_t C_GREENL = rgb565(200, 230, 208);
static const uint16_t C_AMBER  = rgb565(230, 160, 30);

// ─── state ────────────────────────────────────────────────────────────────────
static spi_device_handle_t s_spi = nullptr;
static uint16_t*           s_fb  = nullptr;   // W*H framebuffer in PSRAM

// ─── low-level SPI ──────────────────────────────────────────────────────────--
static void dc(int level) { gpio_set_level((gpio_num_t)PIN_DC, level); }

static void spi_tx(const uint8_t* data, size_t len) {
    if (!len) return;
    spi_transaction_t t = {};
    t.length    = len * 8;
    t.tx_buffer = data;
    spi_device_polling_transmit(s_spi, &t);
}
static void wr_cmd(uint8_t c)  { dc(0); spi_tx(&c, 1); }
static void wr_data(uint8_t d) { dc(1); spi_tx(&d, 1); }
static void wr_data_n(const uint8_t* d, size_t n) { dc(1); spi_tx(d, n); }

static void set_window(int x0, int y0, int x1, int y1) {
    uint8_t cax[4] = { 0, (uint8_t)(x0 + X_OFF), 0, (uint8_t)(x1 + X_OFF) };
    uint8_t ray[4] = { 0, (uint8_t)(y0 + Y_OFF), 0, (uint8_t)(y1 + Y_OFF) };
    wr_cmd(0x2A); wr_data_n(cax, 4);   // CASET
    wr_cmd(0x2B); wr_data_n(ray, 4);   // RASET
    wr_cmd(0x2C);                      // RAMWR
}

static void panel_init() {
    // Reset pulse
    gpio_set_level((gpio_num_t)PIN_RST, 0); vTaskDelay(pdMS_TO_TICKS(20));
    gpio_set_level((gpio_num_t)PIN_RST, 1); vTaskDelay(pdMS_TO_TICKS(120));

    wr_cmd(0x01); vTaskDelay(pdMS_TO_TICKS(150));            // SWRESET
    wr_cmd(0x11); vTaskDelay(pdMS_TO_TICKS(255));            // SLPOUT

    uint8_t frm[3]  = { 0x01, 0x2C, 0x2D };
    wr_cmd(0xB1); wr_data_n(frm, 3);                        // FRMCTR1
    wr_cmd(0xB2); wr_data_n(frm, 3);                        // FRMCTR2
    uint8_t frm3[6] = { 0x01,0x2C,0x2D,0x01,0x2C,0x2D };
    wr_cmd(0xB3); wr_data_n(frm3, 6);                      // FRMCTR3
    wr_cmd(0xB4); wr_data(0x07);                           // INVCTR

    uint8_t pw1[3] = { 0xA2, 0x02, 0x84 };
    wr_cmd(0xC0); wr_data_n(pw1, 3);                       // PWCTR1
    wr_cmd(0xC1); wr_data(0xC5);                           // PWCTR2
    uint8_t pw3[2] = { 0x0A, 0x00 };
    wr_cmd(0xC2); wr_data_n(pw3, 2);                       // PWCTR3
    uint8_t pw4[2] = { 0x8A, 0x2A };
    wr_cmd(0xC3); wr_data_n(pw4, 2);                       // PWCTR4
    uint8_t pw5[2] = { 0x8A, 0xEE };
    wr_cmd(0xC4); wr_data_n(pw5, 2);                       // PWCTR5
    wr_cmd(0xC5); wr_data(0x0E);                           // VMCTR1

    wr_cmd(0x21);                                          // INVON (0.96" IPS)
    wr_cmd(0x36); wr_data(0x08);                           // MADCTL: BGR, portrait
    wr_cmd(0x3A); wr_data(0x05);                           // COLMOD: 16-bit/pixel

    uint8_t gp[16] = { 0x02,0x1c,0x07,0x12,0x37,0x32,0x29,0x2d,
                       0x29,0x25,0x2B,0x39,0x00,0x01,0x03,0x10 };
    wr_cmd(0xE0); wr_data_n(gp, 16);                       // GMCTRP1
    uint8_t gn[16] = { 0x03,0x1d,0x07,0x06,0x2E,0x2C,0x29,0x2D,
                       0x2E,0x2E,0x37,0x3F,0x00,0x00,0x02,0x10 };
    wr_cmd(0xE1); wr_data_n(gn, 16);                       // GMCTRN1

    wr_cmd(0x13); vTaskDelay(pdMS_TO_TICKS(10));            // NORON
    wr_cmd(0x29); vTaskDelay(pdMS_TO_TICKS(100));           // DISPON
}

// Push the whole framebuffer, one row at a time through an internal (DMA-capable)
// bounce buffer — keeps the 25 KB framebuffer in PSRAM and never DMAs from it.
static void flush() {
    static uint8_t line[W * 2];
    for (int y = 0; y < H; ++y) {
        const uint16_t* src = &s_fb[y * W];
        for (int x = 0; x < W; ++x) {
            line[x * 2]     = src[x] >> 8;     // hi byte first (big-endian panel)
            line[x * 2 + 1] = src[x] & 0xFF;
        }
        set_window(0, y, W - 1, y);
        wr_data_n(line, sizeof(line));
    }
}

// ─── framebuffer drawing ──────────────────────────────────────────────────────
static inline void put(int x, int y, uint16_t c) {
    if ((unsigned)x < (unsigned)W && (unsigned)y < (unsigned)H) s_fb[y * W + x] = c;
}
static void fill(uint16_t c) { for (int i = 0; i < W * H; ++i) s_fb[i] = c; }
static void rect(int x0, int y0, int x1, int y1, uint16_t c) {
    for (int y = y0; y < y1; ++y) for (int x = x0; x < x1; ++x) put(x, y, c);
}

static int text_w(const char* s, int scale) { return (int)strlen(s) * 6 * scale; }

static void draw_char(int x, int y, char ch, uint16_t col, int scale) {
    if (ch < 0x20 || ch > 0x7E) ch = ' ';   // outside printable ASCII → blank
    const uint8_t* g = DISPLAY_FONT5X7[ch - 0x20];
    for (int c = 0; c < 5; ++c) {
        uint8_t colbits = g[c];
        for (int r = 0; r < 7; ++r) {
            if (colbits & (1 << r)) {
                for (int dy = 0; dy < scale; ++dy)
                    for (int dx = 0; dx < scale; ++dx)
                        put(x + c * scale + dx, y + r * scale + dy, col);
            }
        }
    }
}
static int draw_text(int x, int y, const char* s, uint16_t col, int scale) {
    for (; *s; ++s) { draw_char(x, y, *s, col, scale); x += 6 * scale; }
    return x;
}
static int center_x(const char* s, int scale) {
    int w = text_w(s, scale);
    return w >= W ? 0 : (W - w) / 2;
}

// Truncate s in place with a trailing '.' if it would exceed maxpx at scale 1.
static void fit(char* s, int maxpx) {
    int n = (int)strlen(s);
    if (n * 6 <= maxpx) return;
    int keep = maxpx / 6;
    if (keep < 1) { s[0] = '\0'; return; }
    s[keep - 1] = '.';
    s[keep]     = '\0';
}

static void wifi_bars(int x, int y, int level) {
    for (int i = 0; i < 4; ++i) {
        int bh = 2 + i * 2;
        uint16_t col = (i < level) ? C_GREEN : C_GREENL;
        rect(x + i * 4, y + (8 - bh), x + i * 4 + 3, y + 8, col);
    }
}

static void ring(int cx, int cy, int rad, float frac, uint16_t col, uint16_t bg) {
    for (int a = 0; a < 360; ++a) {
        float rad_a = a * (float)M_PI / 180.0f;
        bool lit = (a / 360.0f) <= frac;
        for (int w = 0; w < 3; ++w) {
            int rr = rad - w;
            int xx = (int)lroundf(cx + rr * sinf(rad_a));
            int yy = (int)lroundf(cy - rr * cosf(rad_a));
            put(xx, yy, lit ? col : bg);
        }
    }
}

static int rssi_bars(int rssi) {
    if (rssi >= -55) return 4;
    if (rssi >= -65) return 3;
    if (rssi >= -75) return 2;
    if (rssi >= -85) return 1;
    return 0;
}

// ─── compose one frame from cached state (mirrors tools/display_sim.py) ────────
static void compose(VehicleController& v) {
    fill(C_BG);

    // CARD 1 — CONNECTIONS
    rect(3, 3, W - 3, 96, C_CARD);
    draw_text(6, 5, "CONNECTIONS", C_GREY, 1);

    char buf[48];

    // WiFi: bars + RSSI + SSID
    wifi_ap_record_t ap = {};
    if (esp_wifi_sta_get_ap_info(&ap) == ESP_OK) {
        wifi_bars(6, 16, rssi_bars(ap.rssi));
        snprintf(buf, sizeof(buf), "%ddBm", ap.rssi);
        draw_text(24, 16, buf, C_INK, 1);
        snprintf(buf, sizeof(buf), "%s", (const char*)ap.ssid);
        fit(buf, W - 14);
        draw_text(8, 26, buf, C_GREEN, 1);
    } else {
        draw_text(6, 16, "WiFi --", C_GREY, 1);
    }

    // BLE: bars + RSSI + peer (abbreviated to fit 80px)
    int8_t brssi = 0;
    if (v.ble_connected() && v.ble_rssi(brssi)) {
        wifi_bars(6, 38, rssi_bars(brssi));
        snprintf(buf, sizeof(buf), "%ddBm", brssi);
        draw_text(24, 38, buf, C_INK, 1);
        std::string peer = v.ble_peer();
        // keep the tail (the distinctive bytes); 80px fits ~10 chars
        std::string shown = peer.size() > 10 ? (".." + peer.substr(peer.size() - 8)) : peer;
        snprintf(buf, sizeof(buf), "%s", shown.c_str());
        draw_text(8, 48, buf, C_GREEN, 1);
    } else {
        draw_text(6, 38, "BLE --", C_GREY, 1);
    }

    // MQTT: configured broker host:port (no live session required), wrapped
    draw_text(6, 61, "MQTT", C_GREY, 1);
    std::string broker = mqtt_ha_broker();
    if (broker.empty()) {
        draw_text(8, 71, "off", C_GREY, 1);
    } else {
        auto colon = broker.find(':');
        std::string host = (colon == std::string::npos) ? broker : broker.substr(0, colon);
        std::string port = (colon == std::string::npos) ? ""     : broker.substr(colon);
        snprintf(buf, sizeof(buf), "%s", host.c_str());
        fit(buf, W - 6);
        draw_text(4, 71, buf, C_GREEN, 1);
        if (!port.empty()) { snprintf(buf, sizeof(buf), "%s", port.c_str()); draw_text(4, 81, buf, C_GREEN, 1); }
    }

    // CARD 2 — CHARGING hero
    rect(3, 100, W - 3, H - 4, C_CARD);

    VehicleController::LinkState ls = v.link_state();
    if (ls != VehicleController::LinkState::Awake) {
        const char* msg = (ls == VehicleController::LinkState::Asleep)      ? "`Asleep"
                        : (ls == VehicleController::LinkState::Unreachable) ? "Offline"
                        :                                                     "...";
        draw_text(center_x(msg, 1), 125, msg, C_GREY, 1);
        return;
    }

    ChargeStateResult cs = v.get_cached_charge();
    bool charging = cs.charging_state == "Charging";
    const char* title = charging ? "`Charging" : "Connected";
    draw_text(center_x(title, 1), 103, title, C_INK, 1);

    int soc = (int)lroundf(cs.battery_level);
    float frac = soc <= 0 ? 0.0f : (soc >= 100 ? 1.0f : soc / 100.0f);
    ring(W / 2, 130, 23, frac, charging ? C_GREEN : C_GREY, C_GREENL);

    char socbuf[8];
    snprintf(socbuf, sizeof(socbuf), "%d", soc);
    int sw = text_w(socbuf, 2) + 6;            // number @2x + "%" @1x
    int x0 = (W - sw) / 2;
    int nx = draw_text(x0, 124, socbuf, C_GREEN, 2);
    draw_text(nx, 131, "%", C_GREEN, 1);

    // power (kW) + current (A)
    char foot[20];
    snprintf(foot, sizeof(foot), "%dkW   %dA",
             (int)lroundf(cs.charger_power), cs.charging_amps);
    draw_text(center_x(foot, 1), 150, foot, C_INK, 1);
}

// ─── refresh task ──────────────────────────────────────────────────────────--
static void display_task(void* arg) {
    VehicleController& v = *static_cast<VehicleController*>(arg);
    for (;;) {
        compose(v);
        flush();
        vTaskDelay(pdMS_TO_TICKS(1000));
    }
}

void display_start(VehicleController& vehicle) {
    // Framebuffer in PSRAM (free on the T-Dongle-C5's 8 MB); fall back to internal.
    s_fb = (uint16_t*)heap_caps_malloc(W * H * sizeof(uint16_t), MALLOC_CAP_SPIRAM);
    if (!s_fb) s_fb = (uint16_t*)heap_caps_malloc(W * H * sizeof(uint16_t), MALLOC_CAP_8BIT);
    if (!s_fb) { ESP_LOGE(TAG, "no memory for framebuffer — display disabled"); return; }

    gpio_config_t io = {};
    io.mode = GPIO_MODE_OUTPUT;
    io.pin_bit_mask = (1ULL << PIN_DC) | (1ULL << PIN_RST) | (1ULL << PIN_BL);
    gpio_config(&io);
    gpio_set_level((gpio_num_t)PIN_BL, 1);   // backlight on

    spi_bus_config_t bus = {};
    bus.mosi_io_num   = PIN_MOSI;
    bus.miso_io_num   = -1;
    bus.sclk_io_num   = PIN_SCK;
    bus.quadwp_io_num = -1;
    bus.quadhd_io_num = -1;
    bus.max_transfer_sz = W * 2 + 8;
    if (spi_bus_initialize(SPI2_HOST, &bus, SPI_DMA_CH_AUTO) != ESP_OK) {
        ESP_LOGE(TAG, "spi_bus_initialize failed — display disabled"); return;
    }
    spi_device_interface_config_t dev = {};
    dev.clock_speed_hz = 40 * 1000 * 1000;
    dev.mode           = 0;
    dev.spics_io_num   = PIN_CS;
    dev.queue_size     = 7;
    if (spi_bus_add_device(SPI2_HOST, &dev, &s_spi) != ESP_OK) {
        ESP_LOGE(TAG, "spi_bus_add_device failed — display disabled"); return;
    }

    panel_init();
    ESP_LOGI(TAG, "ST7735 %dx%d display up on SPI2 (MOSI%d SCK%d CS%d DC%d)",
             W, H, PIN_MOSI, PIN_SCK, PIN_CS, PIN_DC);

    // 6 KB stack: the C5 has no FPU, so the ring's sinf/cosf go through soft-float,
    // and snprintf adds its own frames — 4 KB would be tight.
    xTaskCreate(display_task, "display", 6144, &vehicle, 3, nullptr);
}

